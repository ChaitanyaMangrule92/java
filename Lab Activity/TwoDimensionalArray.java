package Lab;

public class TwoDimensionalArray {

	 public static void main(String []arg){
		 
	        int a[][]={{10,20,30},{40,50,60}};
	        
	        for(int i=0;i<a[0].length;i++){
	        	
	            for(int j=0;j<a.length;j++){
	            	
	            System.out.print(a[j][i]+"\t");
	           
	        }
	        System.out.println( );
	    }

	}
	
}
